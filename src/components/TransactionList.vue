<template>
    <div>
        <h3>History</h3>
        <ul id="list" class="list">
            <li :class="transaction.amount < 0 ? 'minus' : 'plus'" v-for="transaction in transactions"
                :key="transaction.id">
                {{ transaction.text }} <span>-${{ transaction.amount }}</span><button @click="deleteList(transaction.id)" class="delete-btn">x</button>
            </li>
        </ul>
    </div>
</template>

<script setup>
import { defineProps, defineEmits } from 'vue';

const emit = defineEmits(['delete-list'])

const props = defineProps({
    transactions: {
        type: Array,
        required: true
    }
})

const deleteList = (id) => {
    emit('delete-list', id)
}
</script>